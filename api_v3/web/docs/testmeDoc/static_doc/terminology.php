<p><a name="media-networking-terms"></a></p>
<h2>Media &amp; Networking Terms</h2>
<ul class="bb-list" style="list-style-type: circle;">
	<li>
	<h3><a name="aspect-ratio">Aspect ratio</a></h3>
	<p>The ratio between the width and the height of a video. Most
	common ratios are: 4:3 - Normal screen, 16:9 - wide screen.
	</li>
	<li>
	<h3><a name="bumper-video">Bumper video</a></h3>
	<p>A brief video that plays before a main video starts. Usually
	identifies the organization that provides the main video. Also may be a
	type of custom advertisement.
	</li>
	<li>
	<h3><a name="content-delivery-network"
		href="#content-delivery-network">CDN</a></h3>
	<p>A content delivery network or content distribution network is a
	system of computers networked together across the Internet that
	cooperate transparently to distribute content for the purposes of
	improving performance and scalability. Read more about media streaming
	in Wikipedia.
	</li>
	<li>
	<h3><a name="codec">Codec</a></h3>
	<p>Compressor/decompressor. Technology used to compress and
	decompress computer data, such as audio or video files.
	</li>
	<li>
	<h3><a name="cms">CMS</a></h3>
	<p>Content Management System.
	</li>
	<li>
	<h3><a name="embedding-flash-object"
		href="#embedding-flash-object">Embedding Adobe Flash Object</a></h3>
	<p>Inclusion of an Adobe Flash object within an html page to enable
	its display by an Adobe Flash player plug-in application. The Flash
	object is embedded using the OBJECT and EMBED html tags. <a
		href="http://www.kaltura.org/kdp-basic-usage-embedding-html-pages-and-playback"
		class="bb-url" target="_blank">Learn about KDP Embed...</a>
	</li>
	<li>
	<h3><a name="flv">FLV</a></h3>
	<p>Flash Video is a file format used to deliver video over the
	Internet using Adobe Flash Player or other browser plug-ins. Currently
	the most popular format for embedded video on the web.
	</li>
	<li>
	<h3><a name="fme">FME</a></h3>
	<p>Adobe Flash Media Encoder.
	</li>
	<li>
	<h3><a name="fms">FMS</a></h3>
	<p>Adobe Flash Media Server.
	</li>
	<li>
	<h3><a name="latency">Latency</a></h3>
	<p>(Live video) The delay that a viewer experiences between
	delivery of a video image and its audio signal.<br />
	(Media player) The delay that a viewer experiences between pressing
	Play and viewing a video.
	</li>
	<li>
	<h3><a name="lms">LMS</a></h3>
	<p>Learning Management System.
	</li>
	<li>
	<h3><a name="mid-roll">Mid-roll</a></h3>
	<p>An advertisement that plays at a specific point during a main
	video. The point where the mid-roll plays in the video is configurable.


	
	</li>
	<li>
	<h3><a name="mrss">MRSS</a></h3>
	<p>Media RSS file format.
	</li>
	<li>
	<h3><a name="post-roll">Post roll</a></h3>
	<p>An advertisement that plays after a main video.
	</li>
	<li>
	<h3><a name="pre-roll">Pre-roll</a></h3>
	<p>An advertisement that plays before a main video.
	</li>
	<li>
	<h3><a name="scrubber">Scrubber</a></h3>
	<p>A scale line in a media player. Usually used as a progress bar
	and for moving to a new position.
	</li>
	<li>
	<h3><a name="streaming">Streaming</a></h3>
	<p>Transmission of digital audio or video, or the listening and
	viewing of such data without first storing it. Streaming is mostly
	measured by the amount of bandwidth consumed while serving content.
	Read more about media streaming in Wikipedia.
	</li>
	<li>
	<h3><a name="sunrise">Sunrise</a></h3>
	<p>The time that an entry becomes accessible to end users in the
	destination site.
	</li>
	<li>
	<h3><a name="sunset">Sunset</a></h3>
	<p>The time that an entry becomes inaccessible to end users in the
	destination site.
	</li>
	<li>
	<h3><a name="swf">SWF</a></h3>
	<p>'Small Web Format' by Adobe. This is the dominant format for
	displaying "animated" vector graphics on the web, also supporting
	audio, video and many different possible forms of interaction with the
	end-user. Read More...
	</li>
	<li>
	<h3><a name="swfobject">SWFObject</a></h3>
	<p>An open-source JavaScript library used to embed Adobe Flash
	content within web pages while dealing with player version detection
	and avoiding broken SWF content. <a
		href="http://code.google.com/p/swfobject/" class="bb-url" target="_blank">Read
	More...</a>
	</li>
	<li>
	<h3><a name="thumbnail">Thumbnail</a></h3>
	<p>A small version of an image, or an image representing a video,
	used for preview purposes.
	</li>
	<li>
	<h3><a name="transcoding">Transcoding</a></h3>
	<p>Direct digital-to-digital conversion of one encoding to another.
	Read more about media transcoding in Wikipedia.
	</li>
	<li>
	<h3><a name="ugc">UGC</a></h3>
	<p>User-generated content.
	</li>
	<li>
	<h3><a name="white-labeling">White
	labeling</a></h3>
	<p>The ability for a publisher to brand a Kaltura player or
	application.
	</li>
</ul>
