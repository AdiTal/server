<h1>Kaltura Terminology</h1>

<!-- content -->
<p>
<h2>Scope and Target Audience</h2>
<p>The Kaltura Terminology page introduces some of the terms
commonly used in Kaltura's documentation. The glossary is mainly
targeted to readers of Kaltura technical material.</p>
<p><a name="technical-terminology"></a></p>
<h2>Technical Terms</h2>
<ul class="bb-list" style="list-style-type: circle;">
	<li>
	<h3><a name="agpl">AGPL</a></h3>
	<p>Affero General Public License.
	</li>
	<li>
	<h3><a name="decision-layer">Decision
	layer</a></h3>
	<p>An independent chunk of code that contains the logic for
	choosing between multiple courses of action according to a given input.


	
	</li>
	<li>
	<h3><a name="foss">FOSS</a></h3>
	<p>Free and Open Source Software.
	</li>
	<li>
	<h3><a name="gpl">GPL</a></h3>
	<p>GNU General Public License.
	</li>
	<li>
	<h3><a name="oem">OEM</a></h3>
	<p>Original Equipment Manufacturer.<br />
	<a name="open-source"></a>
	</li>
	<li>
	<h3><a href="#open-source">Open Source</a></h3>
	<p>[<a href="http://en.wikipedia.org/wiki/Open_source"
		class="bb-url" target="_blank">wikipedia</a>][<a
		href="http://www.opensource.org/docs/osd" class="bb-url" target="_blank">OSI</a>]<br />
	Open source is a development method for software that harnesses the
	power of distributed peer review and transparency of process. The
	promise of open source is better quality, higher reliability, more
	flexibility, lower cost, and an end to predatory vendor lock-in.
	</li>
	<li>
	<h3><a name="orm">ORM</a></h3>
	<p>Object-relational mapping. Allows access to a database using a
	set of objects.
	</li>
	<li>
	<h3><a name="var">VAR</a></h3>
	<p>Value-added reseller.
	</li>
</ul>
<p><a name="partnership-terms"></a></p>
<h2>Kaltura Partner ID Terms</h2>
<ul class="bb-list" style="list-style-type: circle;">
	<li>
	<h3><a name="kaltura-web-service-admin-secret"
		href="#kaltura-web-service-admin-secret">Admin Web Service Secret</a></h3>
	<p>A unique admin-level security string assigned to a specific
	Kaltura Partner. This is used by the Kaltura servers as a key for
	authenticating admin web sessions initiated by client-side applications
	utilizing Kaltura technology.
	</li>
	<li>
	<h3><a name="kaltura-network">Kaltura
	Network</a></h3>
	<p>A network of rich-media, video content and it's metadata shared
	by the Kaltura partners and users across the network of Kaltura servers
	on the hosted edition (will be opt-in in CE and EE editions).<br />
	The content is shared under CC to encourage re-use of free content and
	re-mixing.<br />
	It is on an opt-in base, and partners with secured or proprietary
	content can choose to opt-out from being a part of the Kaltura network.<br />
	The Kaltura network enables partners to collaborate, share and remix
	content, increasing syndication and improve visibility for SEO and
	wider audience reach.
	</li>
	<li>
	<h3><a name="kaltura-partner">Kaltura
	Partner</a></h3>
	<p>A person or an organization with an account on the Kaltura
	system. Commonly, it is possible to become a Kaltura Partner either by
	manually signing up, or by registering to Kaltura during the
	installation process of a Kaltura application.
	</li>
	<li>
	<h3><a name="kaltura-partner-account"
		href="#kaltura-partner-account">Partner Account</a></h3>
	<p>All information stored within the Kaltura database related to a
	specific partner. Partner account includes partner identifiers,
	partner's security secrets, information on partner website, partner
	server settings etc.
	</li>
	<li>
	<h3><a name="kaltura-partner-id">Partner
	ID</a></h3>
	<p>A numeric identifier that uniquely identifies a partner in the
	Kaltura database.
	</li>
	<li>
	<h3><a name="publisher">Publisher</a></h3>
	<p>A person or an organization with one or more accounts on the
	Kaltura system. A publisher may have multiple partner IDs.
	</li>
	<li>
	<h3><a name="kaltura-sub-partner-id"
		href="#kaltura-sub-partner-id">Sub Partner ID</a></h3>
	<p>A numeric identifier originally defined to enable sub
	partitioning of Kaltura partnership. For a specific partner: Sub
	Partner ID = 100 X Partner ID.
	</li>
	<li>
	<h3><a name="kaltura-user">User</a></h3>
	<p>This term is commonly used within Kaltura documentation to
	represent an end-user using Kaltura applications within a Partner's
	website.
	</li>
	<li>
	<h3><a name="kaltura-web-service-secret"
		href="#kaltura-web-service-secret">Web Service Secret</a></h3>
	<p>A unique user-level security string assigned to a specific
	Kaltura Partner. This is used by the Kaltura servers as a key for
	authenticating user web sessions initiated by client-side applications
	utilizing Kaltura technology.
	</li>
</ul>
<p><a name="kaltura-technology-terms"></a></p>
<h2>Kaltura Technology Terms</h2>
<ul class="bb-list" style="list-style-type: circle;">
	<li>
	<h3><a name="kaltura-admin-tags">Admin
	Tags</a></h3>
	<p>A non-hierarchical keyword or term assigned to an entry to help
	describe it and allow it to be found again through a search by an
	administrator. Admin tags can only be added by the system
	administrator.
	</li>
	<li>
	<h3><a name="destination">Destination</a></h3>
	<p>A media provider or video sharing site.
	</li>
	<li>
	<h3><a name="distribution">Distribution</a></h3>
	<p>Publishing media entries in a destination site.
	</li>
	<li>
	<h3><a name="kaltura-entry">Entry</a></h3>
	<p>Kaltura's database and API representation of a content entity
	and its metadata.<br />
	Entry types include media, video, audio, image, data, mix, document,
	and playlist.<br />
	Entry metadata includes type, storage location, title, tag, and rating.


	
	</li>
	<li>
	<h3><a name="kaltura-entry-id">Entry
	ID</a></h3>
	<p>A string identifier generated by Kaltura servers to uniquely
	identify one entry within Kaltura database.
	</li>
	<li>
	<h3><a name="entry-point">Entry point</a></h3>
	<p>(Live video) The nearest physical location where a CDN network
	can ingest a live feed to the delivery network.
	</li>
	<li>
	<h3><a name="flavor">Flavor</a></h3>
	<p>A rendition of an entry with specific attributes, such as
	bitrate and dimensions. The entry may be a video, audio, image,
	document, or mix. Commonly used in multiple bitrate adaptive playback.


	
	</li>
	<li>
	<h3><a name="kaltura-api-client-libraries"
		href="#kaltura-api-client-libraries">Kaltura API Client Libraries</a></h3>
	<p>Client side implementations provided by Kaltura to enable
	convenient processing of HTTP requests/responses while using Kaltura
	servers APIs. <a href="http://www.kaltura.org/kaltura-api-client-libraries-guide"
		class="bb-url" target="_blank">Read more about client libraries...</a>
	</li>
	<li>
	<h3><a name="kaltura-client-notifications"
		href="#kaltura-client-notifications">Kaltura Client Notifications</a></h3>
	<p>HTTP POST requests that are sent upon specific media events from
	a specific Kaltura widget to a URL on partners` host servers. Kaltura
	client notifications enable partners to integrate with Kaltura's
	technology in a way that is synchronized with media events occurring on
	Kaltura's servers as well as with Kaltura widget's control events. An
	example to Kaltura client notification is the entry_add notification,
	sent by the KCW widget upon the completion of entry uploading widget
	step. Partner's can process this notification and enable partner
	specific metadata setting for this entry at their web site, aligned
	with the KCW widget's steps
	</li>
	<li>
	<h3><a name="kaltura-metadata">Kaltura
	Metadata</a></h3>
	<p>"Data about other data". This term is commonly used within
	Kaltura documentation to describe the information being stored within
	the Kaltura database with relation to a specific entry. This may
	include: name, tags, admin tags, creation/modification dates,
	description, partner data etc.<br />
	In some cases this may also relate to the <a
		href="http://www.kaltura.org/sdl-sequence-description-language-0" class="bb-url" target="_blank">Kaltura
	Mix SDL</a> (the XML that represent the Kaltura Mix Sequence).
	</li>
	<li>
	<h3><a name="kaltura-server-notifications"
		href="#kaltura-server-notifications">Kaltura Server Notifications</a></h3>
	<p>HTTP POST requests that are sent upon specific media events from
	the Kaltura servers to a URL on partners` host servers. Kaltura server
	notifications enable partners to integrate with Kaltura's technology in
	a way that is synchronized with media events occurring on Kaltura's
	servers. An example to Kaltura server notification is the entry_add
	notification, sent upon the completion of entry creation at Kaltura
	servers. Partner's can process this notification and enable or display
	this entry at their web site, only when it was actually added to
	Kaltura servers.
	</li>
	<li>
	<h3><a name="kaltura-session">Kaltura
	Session (KS)</a></h3>
	<p>This term is commonly used within Kaltura documentation to
	describe the string identifier generated by Kaltura for web session
	authentication. <a href="http://www.kaltura.org/kaltura-api-and-testme-console-introduction"
		class="bb-url" target="_blank">Read more about Kaltura Session...</a>
	</li>
	<li>
	<h3><a name="kaltura-web-services">Kaltura
	Web Services (Kaltura API)</a></h3>
	<p>Kaltura Application Programming Interfaces. These are well
	defined server-side interface methods that expose Kaltura's core
	technology to client-side web implementations. <a
		href="http://www.kaltura.org/kaltura-api-and-testme-console-introduction" class="bb-url" target="_blank">Read
	more about Kaltura APIs...</a>
	</li>
	<li>
	<h3><a name="kaltura-media-entry">Media
	Entry</a></h3>
	<p>An entry representing media content of a specific media type
	(video, video mix, audio, image, etc.)
	</li>
	<li>
	<h3><a name="kaltura-media-filter">Media
	filter</a></h3>
	<p>A set of media entry related parameters and a specific querying
	logic used for conditional selection of media entries from the Kaltura
	database. Media filters are commonly in use at gallery implementations
	for displaying media according to different criteria, such as: media
	uploaded after a specific date, media tagged with specific tags, media
	created by a specific user, etc.
	</li>
	<li>
	<h3><a name="kaltura-partner-services"
		href="#kaltura-partner-services">Partner Services (PS)</a></h3>
	<p>An additional name describing the <a
		href="http://www.kaltura.org/kaltura-api-and-testme-console-introduction" class="bb-url" target="_blank">Kaltura
	API</a>.
	</li>
	<li>
	<h3><a name="kaltura-playlist">Playlist</a></h3>
	<p>A list of entries that were grouped and ordered to be streamed
	one after the other.
	</li>
	<li>
	<h3><a name="kaltura-tags">Tags</a></h3>
	<p>A non-hierarchical keyword or term assigned to an entry to help
	describe it and allow it to be found again through a search.
	</li>
	<li>
	<h3><a name="kaltura-uiconf">uiConf</a></h3>
	<p>This term is commonly used within Kaltura documentation to
	describe the internal configuration xml of a specific Kaltura widget.
	This xml includes the settings of the widget's appearance and
	functionality.
	</li>
	<li>
	<h3><a name="kaltura-video-mix">Video
	Mix</a></h3>
	<p>A video made up of two or more entries.
	</li>
</ul>
